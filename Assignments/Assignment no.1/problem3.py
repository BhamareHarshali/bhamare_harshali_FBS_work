#3. Program to find quotient and remainder of two numbers.

num1 = int(input("Enter the dividend: "))
num2 = int(input("Enter the divisor: "))

quotient = num1 // num2  #dividend 
remainder = num1 % num2  #divisor
 
#Display output 
print (f'dividend is {quotient} and divisor is {remainder} .')