#4. WAP to calculate area of triangle and rectangle

print ("---------Trinagle-----------")

base =float(input("Enter the base of trinagle : "))
height =float(input("Enter the height of triangle : "))

#Area of triangle
area_tri = base * height

#Display output
print(f'Area of triangle : {area_tri}')

print ("---------Rectangle-----------")
length =int(input("Enter the length of rectangle : "))
breadth =int(input("Enter the breadth of the rectangle : "))

#Area of rectangle 
area_rec = length * breadth

#Display output
print(f'Area of rectangle : {area_rec} .')